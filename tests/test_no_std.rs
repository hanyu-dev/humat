#![cfg(not(feature = "std"))]
#![allow(deprecated)]

use human_format_next::Formatter;

macro_rules! test_formatter {
    (
        $formatter:expr;
        $($number:expr => $ideal:expr),*
    ) => {
        $(
            assert_eq!(
                $ideal,
                $formatter.format($number).to_string(),
                concat!("Format `", stringify!($number), "` error")
            );
        )*
    };
}

